				<article>
					<h4>OFERTA ACADÉMICA</h4>
					<div style="text-align:justify">
					<p>En el año 2004 considerando las características de la Cartera de Biología y las modificaciones y adecuaciones de las comentes mundiales sobre diseños curriculares en ciencias, en análisis en los talleres se llegó por consenso a adoptar un modelo de currículo que pueda relacionar estos procesos y ampliar las posibilidades de formación de los estudiantes, permitiendo una inserción de calidad en la práctica profesional, a través de la demanda del mercado y las necesidades del entorno socio-cultural, que es el currículo por competencias.</p>

					<p>El modelo por competencias, forma profesionales que conciban el aprendizaje como un proceso abierto, flexible y permanente, que va más allá de su estadía en la universidad. Considerando que su preparación sea más realista, que retome las necesidades de la sociedad (experiencia social, práctica profesional y desarrollo disciplinar) establece que las cualidades de una formación por competencias profesionales implican una preparación más completa, de acuerdo a las demandas actuales que requieren personas de pensamiento flexible, creativo y abierto al cambio.</p>
