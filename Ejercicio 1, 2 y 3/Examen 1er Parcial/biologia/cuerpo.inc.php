					<h4>OBJETIVOS</h4>
					<div style="text-align:justify">
					<p>- Formar profesionales idóneos en todas las ramas de las ciencias biológicas, que respondan a las necesidades de transformación y de desarrollo local y nacional, con conciencia crítica y con capacidad de manejo de los instrumentos teórico-metodológicos y prácticos.</p>

					<p>- Desarrollar y difundir programas de investigación en las áreas de las ciencias biológicas, particularmente en aquellas de interés de la sociedad, como ecología. Aprovechamiento sostenible de la biodiversidad, solución de problemas ambientales, biología molecular y biotecnología.</p>

					<p>- Participar en el análisis de problemas que afectan al medio ambiente proponiendo soluciones viables en base a la investigación y experiencia.</p>

					<p>- Mejorar permanentemente, en concordancia al avance científico, la calidad de la docencia e investigación a través de la actualización yo perfeccionamiento de su personal docente.</p>

					<p>- Participar eficientemente en proyectos multi e interdisciplinarios en el campo de la competencia.</p>

					<p>- Fortalecer vínculos con universidades del interior y exterior.</p>
					</div>
